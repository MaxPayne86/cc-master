cc_actuator_t *cc_actuator_create(const uint8_t *data, uint32_t *written)
{
    cc_actuator_t *actuator = malloc(sizeof(cc_actuator_t));
    actuator->id = *data++;

    *written = 1;
    return actuator;
}

void cc_actuator_destroy(cc_actuator_t *actuator)
{
    if (actuator)
    {
        free(actuator);
    }
}
